#define _BSP_

#include "bsp.h"

/*******************************************************************************
* Function Name  : delay_us
*******************************************************************************/
void delay_us(u16 us)
{    
	u16 i = 0;

	while(us--)
	{
		i=12;
		while(i--);
	}
}

/*******************************************************************************
* Function Name  : delay_ms
*******************************************************************************/
void delay_ms(u16 ms)
{    
	while(ms--)
	{
		delay_us(1000);
	}
}
