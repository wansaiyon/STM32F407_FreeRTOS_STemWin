/*******************************************************************************
* Macro Definition
*******************************************************************************/
#define LCD_BACKLIGHT	PBout(15)
#define LCD_REG			(*((vu16*)(0x6C000000 + (0<<7))))	/* RS = 0 */
#define LCD_RAM			(*((vu16*)(0x6C000000 + (1<<7))))	/* RS = 1 */

/*******************************************************************************
* Function Name  : lcdWriteReg
*******************************************************************************/
__inline void lcdWriteReg(u16 reg, u16 val)
{
	LCD_REG = reg;
	LCD_RAM = val;
}

/*******************************************************************************
* Function Name  : lcdReadReg
*******************************************************************************/
__inline u16 lcdReadReg(u16 reg)
{
	LCD_REG = reg;
	return LCD_RAM;
}

/*******************************************************************************
* Function Name  : lcdWriteRam
*******************************************************************************/
__inline void lcdWriteRam(u16 val)
{
	LCD_RAM = val;
}

/*******************************************************************************
* Function Name  : lcdReadRam
*******************************************************************************/
__inline u16 lcdReadRam(void)
{
	return LCD_RAM;
}

/*******************************************************************************
* Function Name  : lcdSetDir
* 设置屏幕扫描方向
*******************************************************************************/
__inline void lcdSetDir(void)
{
	u16 val = 0;

#if BSP_LCD_SWAP_RB
	val |= (1<<3);
#endif

#if BSP_LCD_SWAP_XY
	val |= (1<<5);
#endif

#if BSP_LCD_MIRROR_X
	val |= (1<<6);
#endif

#if BSP_LCD_MIRROR_Y
	val |= (1<<7);
#endif

	lcdWriteReg(0x3600, val);
}

